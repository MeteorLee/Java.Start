package loopexample;

public class ForExample2 {
	
	public static void main(String[] args) {
		
		for(int i = 0; i < 10; i++) {
			System.out.println("Hello, World");
			
		}
		
		System.out.println();
		
		for (int i = 1; i <= 10; i++) {
			System.out.println("Hello, World");
		}
	}

}
// 둘다 10번씩 출력하지만
// 0부터 시작하는 위에꺼를 선호하는 버릇이 중요함